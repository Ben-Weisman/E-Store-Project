#include "Utils.h"
#include <iostream>

using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool isLetter(char ch) 
{ // function that return true if the character is a letter
	if ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z'))
		return true;

	return false;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool isNumber(char ch)
{// function that return true if the character is a number
	if (ch >= '0' && ch <= '9')
		return true;

	return false;
}
